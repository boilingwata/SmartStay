begin;

do $$
begin
  if not exists (
    select 1
    from pg_type t
    join pg_namespace n on n.oid = t.typnamespace
    where n.nspname = 'smartstay'
      and t.typname = 'utility_policy_scope'
  ) then
    create type smartstay.utility_policy_scope as enum (
      'system',
      'building',
      'room',
      'contract'
    );
  end if;
end
$$;

do $$
begin
  if not exists (
    select 1
    from pg_type t
    join pg_namespace n on n.oid = t.typnamespace
    where n.nspname = 'smartstay'
      and t.typname = 'billing_run_status'
  ) then
    create type smartstay.billing_run_status as enum (
      'draft',
      'running',
      'completed',
      'failed',
      'cancelled'
    );
  end if;
end
$$;

create or replace function smartstay.touch_utility_updated_at()
returns trigger
language plpgsql
set search_path = smartstay, public
as $$
begin
  new.updated_at := now();
  return new;
end;
$$;

create table if not exists smartstay.utility_policies (
  id bigint generated by default as identity primary key,
  code text not null,
  name text not null,
  scope_type smartstay.utility_policy_scope not null,
  scope_id bigint null,
  is_active boolean not null default true,
  description text null,
  electric_base_amount numeric not null default 0 check (electric_base_amount >= 0),
  water_base_amount numeric not null default 0 check (water_base_amount >= 0),
  water_per_person_amount numeric not null default 0 check (water_per_person_amount >= 0),
  electric_hot_season_multiplier numeric not null default 1.15 check (electric_hot_season_multiplier > 0),
  location_multiplier numeric not null default 1.0 check (location_multiplier > 0),
  season_months jsonb not null default '["04","05","06","07","08","09"]'::jsonb,
  rounding_increment numeric not null default 1000 check (rounding_increment > 0),
  min_electric_floor numeric not null default 120000 check (min_electric_floor >= 0),
  min_water_floor numeric not null default 60000 check (min_water_floor >= 0),
  effective_from date not null default current_date,
  effective_to date null,
  created_by uuid null references smartstay.profiles(id),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint utility_policies_scope_ck check (
    (scope_type = 'system' and scope_id is null)
    or (scope_type <> 'system' and scope_id is not null)
  ),
  constraint utility_policies_effective_range_ck check (
    effective_to is null or effective_to >= effective_from
  ),
  constraint utility_policies_season_months_ck check (
    jsonb_typeof(season_months) = 'array'
  ),
  constraint utility_policies_code_key unique (code)
);

