create table if not exists smartstay.billing_runs (
  id bigint generated by default as identity primary key,
  billing_period text not null,
  status smartstay.billing_run_status not null default 'draft',
  started_at timestamptz null,
  completed_at timestamptz null,
  created_by uuid null references smartstay.profiles(id),
  lock_version integer not null default 0,
  summary_json jsonb not null default '{}'::jsonb,
  error_json jsonb not null default '{}'::jsonb,
  notes text null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint billing_runs_billing_period_ck check (billing_period ~ '^\d{4}-\d{2}$'),
  constraint billing_runs_completed_after_started_ck check (
    completed_at is null or started_at is null or completed_at >= started_at
  ),
  constraint billing_runs_billing_period_key unique (billing_period)
);

