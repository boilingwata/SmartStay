create or replace function smartstay.map_payment_attempt_method(p_method text)
returns smartstay.payment_attempt_method
language plpgsql
immutable
set search_path = smartstay, public
as $$
begin
  case lower(coalesce(trim(p_method), ''))
    when 'momo', 'momo_online' then
      return 'momo_online'::smartstay.payment_attempt_method;
    when 'cash', 'tien_mat' then
      return 'tien_mat'::smartstay.payment_attempt_method;
    when 'bank_transfer', 'chuyen_khoan' then
      return 'chuyen_khoan'::smartstay.payment_attempt_method;
    else
      raise exception 'Unsupported payment method: %', p_method;
  end case;
end;
$$;

