from (
  select distinct on (ct.contract_id)
    ct.contract_id,
    ct.tenant_id
  from smartstay.contract_tenants ct
  order by ct.contract_id, ct.is_primary desc, ct.id asc
) src
where c.id = src.contract_id
  and c.primary_tenant_id is null;

