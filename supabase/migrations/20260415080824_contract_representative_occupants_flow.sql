insert into smartstay.room_occupants (
  contract_id,
  room_id,
  tenant_id,
  is_primary_tenant,
  move_in_at,
  move_out_at,
  status
)
select
  ct.contract_id,
  c.room_id,
  ct.tenant_id,
  coalesce(ct.is_primary, false),
  c.start_date,
  case
    when c.status in ('active', 'pending_signature') and coalesce(c.is_deleted, false) = false then null
    else c.end_date
  end,
  case
    when c.status in ('active', 'pending_signature') and coalesce(c.is_deleted, false) = false then 'active'::smartstay.occupant_status
    else 'moved_out'::smartstay.occupant_status
  end
from smartstay.contract_tenants ct
join smartstay.contracts c on c.id = ct.contract_id
on conflict (contract_id, tenant_id) do nothing;

