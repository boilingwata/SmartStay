create table if not exists smartstay.amenity_catalog (
  id bigint generated by default as identity primary key,
  legacy_service_id integer unique null references smartstay.services(id) on delete set null,
  code text not null unique,
  name text not null,
  description text null,
  building_id bigint null references smartstay.buildings(id) on delete set null,
  is_active boolean not null default true,
  is_deleted boolean not null default false,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists smartstay.service_catalog (
  id bigint generated by default as identity primary key,
  legacy_service_id integer unique null references smartstay.services(id) on delete set null,
  code text not null unique,
  name text not null,
  description text null,
  service_kind text not null default 'fixed_recurring'
    check (service_kind in ('fixed_recurring')),
  billing_method text not null default 'fixed'
    check (billing_method in ('fixed', 'per_person', 'per_room', 'per_unit')),
  unit text not null default 'thang',
  is_active boolean not null default true,
  is_deleted boolean not null default false,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists smartstay.amenity_booking_charges (
  id bigint generated by default as identity primary key,
  booking_id bigint not null references smartstay.amenity_bookings(id) on delete cascade,
  invoice_id integer null references smartstay.invoices(id) on delete set null,
  billing_period text null,
  amount numeric(12,2) not null default 0 check (amount >= 0),
  status text not null default 'pending'
    check (status in ('pending', 'invoiced', 'waived', 'cancelled')),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

