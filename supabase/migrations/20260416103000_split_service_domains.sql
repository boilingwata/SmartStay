create or replace function smartstay.create_contract_v2(
  p_room_id integer,
  p_start_date date,
  p_end_date date,
  p_monthly_rent numeric,
  p_deposit_amount numeric default 0,
  p_payment_cycle_months integer default 1,
  p_primary_tenant_id integer default null,
  p_occupant_ids integer[] default '{}'::integer[],
  p_payment_due_day smallint default 5,
  p_utility_policy_id bigint default null,
  p_service_ids bigint[] default '{}'::bigint[],
  p_service_prices numeric[] default '{}'::numeric[],
  p_service_quantities numeric[] default '{}'::numeric[],
  p_mark_deposit_received boolean default false
) returns jsonb
language plpgsql
security definer
set search_path = smartstay, public
as $$
declare
  v_contract_id integer;
  v_contract_code text;
  v_idx integer;
begin
  if p_room_id is null then
    raise exception 'room_id is required';
  end if;

  insert into smartstay.contracts (
    room_id,
    start_date,
    end_date,
    payment_cycle_months,
    payment_due_day,
    monthly_rent,
    deposit_amount,
    deposit_status,
    status,
    primary_tenant_id,
    occupants_for_billing,
    utility_billing_type,
    utility_policy_id,
    terms
  )
  values (
    p_room_id,
    p_start_date,
    p_end_date,
    coalesce(p_payment_cycle_months, 1),
    coalesce(p_payment_due_day, 5),
    p_monthly_rent,
    coalesce(p_deposit_amount, 0),
    case when coalesce(p_mark_deposit_received, false) then 'paid'::smartstay.deposit_status else 'pending'::smartstay.deposit_status end,
    'active'::smartstay.contract_status,
    p_primary_tenant_id,
    greatest(1, coalesce(array_length(p_occupant_ids, 1), 0) + case when p_primary_tenant_id is null then 0 else 1 end),
    'policy',
    p_utility_policy_id,
    jsonb_build_object(
      'occupants_for_billing',
      greatest(1, coalesce(array_length(p_occupant_ids, 1), 0) + case when p_primary_tenant_id is null then 0 else 1 end)
    )
  )
  returning id, contract_code into v_contract_id, v_contract_code;

  if p_primary_tenant_id is not null then
    insert into smartstay.contract_tenants (contract_id, tenant_id, is_primary)
    values (v_contract_id, p_primary_tenant_id, true)
    on conflict do nothing;

    insert into smartstay.room_occupants (
      contract_id,
      room_id,
      tenant_id,
      is_primary_tenant,
      move_in_at,
      status
    )
    values (
      v_contract_id,
      p_room_id,
      p_primary_tenant_id,
      true,
      p_start_date,
      'active'
    )
    on conflict do nothing;
  end if;

  if coalesce(array_length(p_occupant_ids, 1), 0) > 0 then
    insert into smartstay.contract_tenants (contract_id, tenant_id, is_primary)
    select v_contract_id, occupant_id, false
    from unnest(p_occupant_ids) as occupant_id
    on conflict do nothing;

    insert into smartstay.room_occupants (
      contract_id,
      room_id,
      tenant_id,
      is_primary_tenant,
      move_in_at,
      status
    )
    select
      v_contract_id,
      p_room_id,
      occupant_id,
      false,
      p_start_date,
      'active'
    from unnest(p_occupant_ids) as occupant_id
    on conflict do nothing;
  end if;

  if coalesce(array_length(p_service_ids, 1), 0) > 0 then
    for v_idx in 1..array_length(p_service_ids, 1) loop
      insert into smartstay.contract_services (
        contract_id,
        service_catalog_id,
        fixed_price,
        quantity
      )
      values (
        v_contract_id,
        p_service_ids[v_idx],
        coalesce(p_service_prices[v_idx], 0),
        coalesce(p_service_quantities[v_idx], 1)
      )
      on conflict (contract_id, service_catalog_id) do update
      set
        fixed_price = excluded.fixed_price,
        quantity = excluded.quantity;
    end loop;
  end if;

  update smartstay.rooms
  set status = 'occupied'::smartstay.room_status, updated_at = now()
  where id = p_room_id;

  return jsonb_build_object(
    'contractId', v_contract_id,
    'contractCode', v_contract_code
  );
end;
$$;

