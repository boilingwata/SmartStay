create or replace function smartstay.sync_amenity_legacy_refs()
returns trigger
language plpgsql
set search_path = smartstay, public
as $$
begin
  if new.amenity_id is null and new.service_id is not null then
    select ac.id
      into new.amenity_id
    from smartstay.amenity_catalog ac
    where ac.legacy_service_id = new.service_id
    limit 1;
  end if;

  if new.service_id is null and new.amenity_id is not null then
    select ac.legacy_service_id
      into new.service_id
    from smartstay.amenity_catalog ac
    where ac.id = new.amenity_id
    limit 1;
  end if;

  return new;
end;
$$;

create or replace function smartstay.sync_service_catalog_legacy_refs()
returns trigger
language plpgsql
set search_path = smartstay, public
as $$
begin
  if new.service_catalog_id is null and new.service_id is not null then
    select sc.id
      into new.service_catalog_id
    from smartstay.service_catalog sc
    where sc.legacy_service_id = new.service_id
    limit 1;
  end if;

  if new.service_id is null and new.service_catalog_id is not null then
    select sc.legacy_service_id
      into new.service_id
    from smartstay.service_catalog sc
    where sc.id = new.service_catalog_id
    limit 1;
  end if;

  return new;
end;
$$;

