with amenity_source as (
  select distinct s.id, s.name, s.is_active, s.is_deleted, s.created_at, s.updated_at
  from smartstay.services s
  where exists (select 1 from smartstay.amenity_policies ap where ap.service_id = s.id)
     or exists (select 1 from smartstay.amenity_bookings ab where ab.service_id = s.id)
     or exists (select 1 from smartstay.amenity_booking_exceptions abe where abe.service_id = s.id)
)
insert into smartstay.amenity_catalog (
  id,
  legacy_service_id,
  code,
  name,
  is_active,
  is_deleted,
  created_at,
  updated_at
)
select
  src.id::bigint,
  src.id,
  concat('AMN-', lpad(src.id::text, 4, '0')),
  src.name,
  coalesce(src.is_active, true),
  coalesce(src.is_deleted, false),
  coalesce(src.created_at, now()),
  coalesce(src.updated_at, now())
from amenity_source src
on conflict (id) do update
set
  legacy_service_id = excluded.legacy_service_id,
  name = excluded.name,
  is_active = excluded.is_active,
  is_deleted = excluded.is_deleted,
  updated_at = now();

with service_source as (
  select s.id, s.name, s.calc_type, s.is_active, s.is_deleted, s.created_at, s.updated_at
  from smartstay.services s
  where lower(s.name) not in ('electricity', 'water', 'electric', 'dien', 'nuoc')
)
insert into smartstay.service_catalog (
  id,
  legacy_service_id,
  code,
  name,
  service_kind,
  billing_method,
  unit,
  is_active,
  is_deleted,
  created_at,
  updated_at
)
select
  src.id::bigint,
  src.id,
  concat('SRV-', lpad(src.id::text, 4, '0')),
  src.name,
  'fixed_recurring',
  case src.calc_type
    when 'per_person' then 'per_person'
    when 'per_room' then 'per_room'
    when 'per_unit' then 'per_unit'
    else 'fixed'
  end,
  case src.calc_type
    when 'per_person' then 'nguoi/thang'
    when 'per_room' then 'phong/thang'
    when 'per_unit' then 'so_luong'
    else 'thang'
  end,
  coalesce(src.is_active, true),
  coalesce(src.is_deleted, false),
  coalesce(src.created_at, now()),
  coalesce(src.updated_at, now())
from service_source src
on conflict (id) do update
set
  legacy_service_id = excluded.legacy_service_id,
  name = excluded.name,
  billing_method = excluded.billing_method,
  unit = excluded.unit,
  is_active = excluded.is_active,
  is_deleted = excluded.is_deleted,
  updated_at = now();

